def run_Game():
    global game_over, score, scroll, bg_scroll, fade_counter, high_score
    #player instance
    jumpy = Player(SCREEN_WIDTH // 2, SCREEN_HEIGHT - 150)
    #create sprite groups
    platform_group = pygame.sprite.Group()
    #create starting platform
    platform = Platform(SCREEN_WIDTH // 2 - 50, SCREEN_HEIGHT - 50, 100, False)
    platform_group.add(platform)
    #game loop
    run = True
    while run:
        clock.tick(FPS)
        if game_over == False:
            scroll = jumpy.move()
            #draw background
            bg_scroll += scroll
            if bg_scroll >= 600:
                bg_scroll = 0
            draw_bg(bg_scroll)

            #generate platforms
            if len(platform_group) < MAX_PLATFORMS:
                p_w = random.randint(90, 120)
                p_x = random.randint(0, SCREEN_WIDTH - p_w)
                p_y = platform.rect.y - random.randint(120, 150)
                p_type = 1
                if p_type == 1:
                    p_moving = True
                else:
                    p_moving = False
                platform = Platform(p_x, p_y, p_w, p_moving)
                platform_group.add(platform)
            #update platforms
            platform_group.update(scroll)
            #update score
            if scroll > 0:
                score += scroll
            #draw line at previous high score
            pygame.draw.line(screen, WHITE, (0, score - high_score + SCROLL_THRESH), (SCREEN_WIDTH, score - high_score + SCROLL_THRESH), 3)
            draw_text('HIGH SCORE', font_small, WHITE, SCREEN_WIDTH - 130, score - high_score + SCROLL_THRESH)
            #draw sprites
            platform_group.draw(screen)
            jumpy.draw()
            #draw panel
            draw_panel()
            #check game over
            if jumpy.rect.top > SCREEN_HEIGHT:
                game_over = True
        else:
            if fade_counter < SCREEN_WIDTH:
                fade_counter += 5
                for y in range(0, 6, 2):
                    pygame.draw.rect(screen, BLACK, (0, y * 100, fade_counter, 100))
                    pygame.draw.rect(screen, BLACK, (SCREEN_WIDTH - fade_counter, (y + 1) * 100, SCREEN_WIDTH, 100))
            else:
                draw_text('GAME OVER!', font_big, WHITE, 130, 200)
                draw_text('SCORE: ' + str(score), font_big, WHITE, 130, 250)
                draw_text('PRESS SPACE TO PLAY AGAIN', font_big, WHITE, 40, 300)
                #update high score
                if score > high_score:
                    high_score = score
                    with open('score.txt', 'w') as file:
                        file.write(str(high_score))
                key = pygame.key.get_pressed()
                if key[pygame.K_SPACE]:
                    #reset variables
                    game_over = False
                    score = 0
                    scroll = 0
                    fade_counter = 0
                    #reposition jumpy
                    jumpy.rect.center = (SCREEN_WIDTH // 2, SCREEN_HEIGHT - 150)
                    #reset platforms
                    platform_group.empty()
                    #create starting platform
                    platform = Platform(SCREEN_WIDTH // 2 - 50, SCREEN_HEIGHT - 50, 100, False)
                    platform_group.add(platform)
        #event handler
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                #update high score
                if score > high_score:
                    high_score = score
                    with open('score.txt', 'w') as file:
                        file.write(str(high_score))
                run = False
        #update display window
        pygame.display.update()
    pygame.quit()

